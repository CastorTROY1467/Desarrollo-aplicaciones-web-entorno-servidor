public class Eiercicio11 {
	public static void main(String[] args) {
		int[] nums = {1, 2, 3, 4, 5, 6};
		char[] chars = {'a', 'b', 'c', 'd', 'f', 'g'};
		
		System.out.println("ARRAY 1");
		
		for(int num : nums) {
			System.out.println(num);
		}
		
		System.out.println("\nARRAY 2");
		for(char c : chars) {
			System.out.println(c);
		}
		
	}
}